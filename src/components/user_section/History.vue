<template>
    <ul class="nav nav-pills">
        <li class="nav-item">
            <router-link :to="'/user/' + username" class="nav-link fw-bold  theme-color">Profile</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/history'" class="nav-link fw-bold theme-btn  text-white">Listening
                History</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/tracks'" class="nav-link fw-bold  theme-color">Favorite
                Tracks</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/playlists'"
                class="nav-link fw-bold  theme-color">Playlists</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/albums'" class="nav-link fw-bold  theme-color">Albums</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/artists'" class="nav-link fw-bold  theme-color">Artists</router-link>
        </li>
        <li class="nav-item">
            <router-link :to="'/user/' + username + '/friends'" class="nav-link fw-bold  theme-color">Friends</router-link>
        </li>
    </ul>
    <hr />
    <ul class="list-group">
        <li class="list-group-item rounded mb-1 theme-btn text-light d-flex">
            <div class="d-flex w-100 justify-content-between">
                <div>
                    <span class="fw-bold">Recently played</span>
                </div>
            </div>
        </li>
        <li v-if="tracks.length == 0"
            class="list-group-item theme-list-item-no-hover foreground d-flex justify-content-between">
            <div class="d-flex flex-fill align-items-center">
                <div class="d-flex flex-column">
                    <span class="theme-color fw-bold">No tracks found</span>
                </div>
            </div>
        </li>
        <li class="list-group-item theme-list-item clickable rounded d-flex justify-content-between p-1"
            v-for="track in tracks" @contextmenu.prevent="right_click({ item: track, event: $event })"
            @click="playTrack(track)">
            <div class="d-flex flex-fill foreground align-items-center">
                <div class="ph-track">
                    <img :src="get_cover(track.cover)" class="track-cover theme-border rounded" @error="placeholder" />
                </div>
                <div class="d-flex align-items-center">
                    <button class="btn btn-link search-link d-flex text-start py-0" :content_id="track.id"
                        :content_type="track.type" @click="playTrack(track.id)" style="display:contents;">
                        <span class="theme-color text-break">{{
                            track.title }}</span>
                    </button>
                </div>
            </div>
        </li>
    </ul>
    <div class="d-flex justify-content-end mt-2">
        <button v-show="searchFinished" type="button" class="btn theme-btn  text-white fw-bold"
            @click="setup">Refresh</button>
        <button v-show="!searchFinished" class="btn btn-dark" type="button" disabled>
            <span class="spinner-border spinner-border-sm" role="status" aria-hidden="true"></span>
            Loading...
        </button>
    </div>
</template>

<script setup>
import { ref, onMounted } from 'vue';
import { useRouter } from 'vue-router';
import { right_click, action } from '/js/events.js';

// Federated
const domain = ref(null);

const order = ref([]);
const tracks = ref([]);
const searchFinished = ref(true);

const router = useRouter();
const username = ref(router.currentRoute.value.params.id);

function get_cover(cover) {
    if (!cover) {
        return "/images/track.svg"
    }

    if (cover.startsWith("http")) {
        return cover;
    }

    return ft.server + '/' + cover;
}

async function placeholder(obj) {
    obj.target.remove();
}

// Must be synchronized in groupSession: ok
async function playTrack(track) {
    // Federated
    if (track.server) {
        action({
            func: async function op() {
                ft.playTrack(track.uuid, track.server)
            },
            object: [track.uuid, track.server],
            operation: "playTrack"
        })
        return
    }

    action({
        func: async function op() {
            ft.playTrack(track.id)
        },
        object: [track.id],
        operation: "playTrack"
    })
    return;
}

async function get_federated_tracks(track_ids) {
    // Categorize ids by domain
    let domains = {};
    for (let track_id of track_ids) {
        let [id, domain] = track_id.split('@');
        if (!domains[domain]) {
            domains[domain] = [];
        }
        domains[domain].push(parseInt(id));
    }

    // Get federated tracks from each domain
    for (let domain in domains) {
        let data = await ft.get_federated_tracks(domain, domains[domain]);
        if (!data) return;

        data.tracks.map(track => track.server = domain);
        for (let i = 0; i < order.value.length; i++) {
            let track_id = order.value[i];
            let found_tracks = data.tracks.filter(t => `${t.id}@${t.server}` == track_id);
            if (found_tracks.length) {
                tracks.value[i] = found_tracks[0];
            }
        }
    }
}

async function get_history(id) {
    if (!searchFinished.value) {
        return
    }
    searchFinished.value = false;

    let data = await ft.API(`/user/${id}/history`);
    if (!data) return;

    // Push tracks placeholders
    tracks.value = [];
    for (let i = 0; i < data.order.length; i++) {
        tracks.value.push({});
    }

    order.value = data.order;

    // Get federated tracks
    get_federated_tracks(data.federated);

    // Get local tracks
    for (let i = 0; i < order.value.length; i++) {
        let track_id = order.value[i];
        let tracks_found = data.tracks.filter(t => t.id == track_id);
        if (tracks_found.length) {
            tracks.value[i] = tracks_found[0];
        }
    }

    searchFinished.value = true;
}

async function get_federated_history(id) {
    if (!searchFinished.value) {
        return
    }
    searchFinished.value = false;

    let data = await ft.fAPI(domain.value, `/user/${id}/history`);
    if (!data) return;

    // Push tracks placeholders
    tracks.value = [];
    for (let i = 0; i < data.order.length; i++) {
        tracks.value.push({});
    }

    order.value = data.order;

    // Get federated tracks
    get_federated_tracks(data.federated);

    // Get local tracks
    for (let i = 0; i < order.value.length; i++) {
        let track_id = order.value[i];
        let tracks_found = data.tracks.filter(t => t.id == track_id);
        if (tracks_found.length) {
            tracks.value[i] = tracks_found[0];
        }
    }

    searchFinished.value = true;
}

async function setup() {
    let id = router.currentRoute.value.params.id;
    if (id.includes('@')) {
        [id, domain.value] = id.split('@');
        get_federated_history(id);
        return
    }
    get_history(id);
}

onMounted(() => {
    setup();
})
</script>